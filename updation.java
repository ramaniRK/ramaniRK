public class updation {
	public static void main (String args[]) {
		
		int num = 300;
		
		num = num + 50;
		System.out.println(num);
		
		
		num = num - 60;
		System.out.println(num); 
		
		
		num = num * 4;
		System.out.println(num);
		
		num = num/10;
		System.out.println(num); 
		
		
	}
}
